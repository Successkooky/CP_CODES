import java.util.*;
public class CodeChef65 {
    public static void main(String args[]){
        Scanner input=new Scanner(System.in);
        int X=input.nextInt();
        int Y=input.nextInt();
        int H=input.nextInt();
        int tot=X+((H-1)*Y);
        System.out.println(tot);
    }
    
}
